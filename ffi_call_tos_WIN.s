SECTION .text
;Poo Poo FFI for Windows64 to TempleOS
GLOBAL FFI_CALL_TOS_0
FFI_CALL_TOS_0:
PUSH RBP
MOV RBP,RSP
AND RSP,-0x10
PUSH RBX
CALL RCX
POP RBX
LEAVE
RET

