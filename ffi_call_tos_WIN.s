; https://docs.microsoft.com/en-us/cpp/build/stack-usage?view=msvc-170
; Fist stack arg:0x30
; R9 HOME+0x28 
; R8 HOME+0x20
; RDX HOME+0x18
; RCX HOME+0x10
; RET ADDR+8
; RBP
GLOBAL FFI_CALL_TOS_4
FFI_CALL_TOS_4:
PUSH RBP
MOV RBP,RSP
AND RSP,-0x10
PUSH RBX
PUSH QWORD [RBP+0x30]
